# ─────────────────────────────────────────────────────────────────────────────
# NONMEM Control Stream Generation
# ─────────────────────────────────────────────────────────────────────────────

def generate_nonmem_control_stream(
    model_type: str,
    dataset_path: str,
    output_path: str = "./run001.ctl",
    pk_parameters: dict = None,
    error_model: str = "proportional",
    covariates: list = None,
    omega_structure: str = "diagonal",
    estimation_method: str = "FOCE-I",
    n_subjects_approx: int = 100,
) -> str:
    """Generate a NONMEM control stream (.ctl) file for population PK/PD analysis.

    Supports 1-, 2-, and 3-compartment PK models (IV and oral), Emax PD,
    and indirect response models. The generated file is ready to run with
    NONMEM 7.4+.

    Parameters
    ----------
    model_type : str
        Model structure: '1cmt_iv', '1cmt_oral', '2cmt_iv', '2cmt_oral',
        '3cmt_iv', '3cmt_oral', 'emax', 'indirect_response'.
    dataset_path : str
        Path to the NONMEM-formatted dataset (.csv with ID, TIME, DV, AMT, etc.).
    output_path : str
        Where to write the .ctl file.
    pk_parameters : dict
        Initial estimates for PK parameters. Keys depend on model_type.
        Example for 2cmt_oral: {'CL': 5, 'V1': 50, 'Q': 2, 'V2': 100, 'KA': 1.0}
    error_model : str
        Residual error: 'additive', 'proportional', 'combined'.
    covariates : list
        Covariate effects to include. E.g. ['WT on CL', 'WT on V1', 'AGE on CL'].
    omega_structure : str
        IIV structure: 'diagonal' or 'block'.
    estimation_method : str
        NONMEM estimation: 'FO', 'FOCE', 'FOCE-I', 'SAEM', 'BAYES'.
    n_subjects_approx : int
        Approximate N for $SIZES recommendation.
    """
    log = ["=" * 60]
    log.append("NONMEM CONTROL STREAM GENERATOR")
    log.append("=" * 60)
    log.append(f"Model type         : {model_type}")
    log.append(f"Dataset            : {dataset_path}")
    log.append(f"Error model        : {error_model}")
    log.append(f"Estimation method  : {estimation_method}")

    defaults = {
        "1cmt_iv":    {"CL": 5.0,  "V": 50.0},
        "1cmt_oral":  {"CL": 5.0,  "V": 50.0,  "KA": 1.0},
        "2cmt_iv":    {"CL": 5.0,  "V1": 30.0, "Q": 2.0,  "V2": 80.0},
        "2cmt_oral":  {"CL": 5.0,  "V1": 30.0, "Q": 2.0,  "V2": 80.0, "KA": 1.0},
        "3cmt_iv":    {"CL": 5.0,  "V1": 10.0, "Q2": 2.0, "V2": 60.0, "Q3": 1.0, "V3": 200.0},
        "3cmt_oral":  {"CL": 5.0,  "V1": 10.0, "Q2": 2.0, "V2": 60.0, "Q3": 1.0, "V3": 200.0, "KA": 1.0},
        "emax":       {"EMAX": 100.0, "EC50": 1.0, "E0": 5.0},
        "indirect_response": {"EMAX": 2.0, "EC50": 1.0, "KIN": 10.0, "KOUT": 0.1},
    }
    params = pk_parameters or defaults.get(model_type, {"CL": 5.0, "V": 50.0})

    # Build NONMEM blocks
    ctl_lines = []

    ctl_lines.append(f"; NONMEM Control Stream — {model_type.upper()}")
    ctl_lines.append("; Generated by Biomni PKPD Tools")
    ctl_lines.append(f"; Dataset: {dataset_path}")
    ctl_lines.append("")
    ctl_lines.append(f"$PROBLEM {model_type.upper()} Population PK Analysis")
    ctl_lines.append("")
    ctl_lines.append("$INPUT ID TIME DV AMT EVID MDV CMT RATE" +
                     (" WT AGE SEX" if covariates else ""))
    ctl_lines.append(f"$DATA {dataset_path} IGNORE=@")
    ctl_lines.append("")
    ctl_lines.append(f"$SIZES LIM6={max(200, n_subjects_approx * 3)}")
    ctl_lines.append("")

    # SUBROUTINE selection
    subroutine_map = {
        "1cmt_iv": "ADVAN1 TRANS2",
        "1cmt_oral": "ADVAN2 TRANS2",
        "2cmt_iv": "ADVAN3 TRANS4",
        "2cmt_oral": "ADVAN4 TRANS4",
        "3cmt_iv": "ADVAN11 TRANS4",
        "3cmt_oral": "ADVAN12 TRANS4",
    }

    if model_type in subroutine_map:
        ctl_lines.append(f"$SUBROUTINE {subroutine_map[model_type]}")
        ctl_lines.append("")
        ctl_lines.append("$PK")
        ctl_lines.append("  ; Individual PK parameters with IIV")
        for i, (name, val) in enumerate(params.items(), 1):
            ctl_lines.append(f"  TV{name}  = THETA({i})")
            ctl_lines.append(f"  {name}    = TV{name} * EXP(ETA({i}))")
        if covariates:
            ctl_lines.append("")
            ctl_lines.append("  ; Covariate effects")
            for cov in covariates:
                parts = cov.split(" on ")
                if len(parts) == 2:
                    cov_var, pk_par = parts[0].strip(), parts[1].strip()
                    ctl_lines.append(f"  ; {cov_var} effect on {pk_par}")
                    if "WT" in cov_var:
                        ctl_lines.append(
                            f"  {pk_par} = TV{pk_par} * (WT/70)**THETA({len(params)+covariates.index(cov)+1}) * EXP(ETA({covariates.index(cov)+1}))"
                        )
        ctl_lines.append("")
    else:
        # ODE-based models (Emax, indirect response)
        ctl_lines.append("$SUBROUTINE ADVAN13 TOL=6")
        ctl_lines.append("")
        ctl_lines.append("$MODEL NCOMPARTMENTS=2")
        ctl_lines.append("")
        ctl_lines.append("$PK")
        for i, (name, val) in enumerate(params.items(), 1):
            ctl_lines.append(f"  {name} = THETA({i}) * EXP(ETA({i}))")
        ctl_lines.append("")
        ctl_lines.append("$DES")
        if model_type == "emax":
            ctl_lines.append("  CONC = A(1) / V")
            ctl_lines.append("  EFFECT = E0 + EMAX * CONC / (EC50 + CONC)")
            ctl_lines.append("  DADT(1) = -CL * CONC")
        elif model_type == "indirect_response":
            ctl_lines.append("  CONC   = A(1) / V")
            ctl_lines.append("  INHIB  = EMAX * CONC / (EC50 + CONC)")
            ctl_lines.append("  DADT(1) = -CL * CONC")
            ctl_lines.append("  DADT(2) = KIN * (1 - INHIB) - KOUT * A(2)")
        ctl_lines.append("")

    # ERROR block
    ctl_lines.append("$ERROR")
    ctl_lines.append("  IPRED = F")
    if error_model == "proportional":
        ctl_lines.append("  W     = IPRED * THETA(999)")
        ctl_lines.append("  Y     = IPRED + W * EPS(1)")
        ctl_lines.append("  IWRES = (DV - IPRED) / W")
    elif error_model == "additive":
        ctl_lines.append("  W     = THETA(999)")
        ctl_lines.append("  Y     = IPRED + W * EPS(1)")
        ctl_lines.append("  IWRES = (DV - IPRED) / W")
    else:  # combined
        ctl_lines.append("  W     = SQRT((THETA(999)*IPRED)**2 + THETA(1000)**2)")
        ctl_lines.append("  Y     = IPRED + W * EPS(1)")
        ctl_lines.append("  IWRES = (DV - IPRED) / W")
    ctl_lines.append("")

    # THETA initial estimates
    ctl_lines.append("$THETA")
    for name, val in params.items():
        ctl_lines.append(f"  (0, {val:.3g})      ; TV{name}")
    ctl_lines.append("  (0, 0.1)           ; Proportional error (sigma/RV)")
    ctl_lines.append("")

    # OMEGA block
    ctl_lines.append("$OMEGA")
    if omega_structure == "block" and len(params) >= 2:
        n = len(params)
        ctl_lines.append(f"$OMEGA BLOCK({n})")
        for i in range(n):
            row = "  " + "  ".join(
                ["0.04" if j == i else "0.01" for j in range(i + 1)]
            )
            ctl_lines.append(row + f"  ; ETA({i+1})")
    else:
        for i, name in enumerate(params.keys(), 1):
            ctl_lines.append(f"  0.04              ; IIV-{name} (~20% CV)")
    ctl_lines.append("")
    ctl_lines.append("$SIGMA")
    ctl_lines.append("  1 FIXED           ; Proportional error placeholder")
    ctl_lines.append("")

    # Estimation
    method_map = {
        "FO": "0", "FOCE": "1", "FOCE-I": "1 INTERACTION",
        "SAEM": "SAEM", "BAYES": "BAYES NBURN=1000 NITER=3000",
    }
    est_str = method_map.get(estimation_method, "1 INTERACTION")
    ctl_lines.append(f"$ESTIMATION METHOD={est_str} MAXEVAL=9999 PRINT=5 NOABORT")
    ctl_lines.append("  SIGDIGITS=3 POSTHOC")
    ctl_lines.append("")
    ctl_lines.append("$COVARIANCE PRINT=E UNCONDITIONAL")
    ctl_lines.append("")
    ctl_lines.append("$TABLE ID TIME DV AMT EVID MDV CMT")
    ctl_lines.append("  IPRED CWRES IWRES NOPRINT ONEHEADER")
    ctl_lines.append("  FILE=sdtab001")
    ctl_lines.append("")
    ctl_lines.append("$TABLE ID ETA(1)")
    for i, name in enumerate(params.keys(), 2):
        ctl_lines.append(f"  ETA({i})")
    ctl_lines.append("  NOPRINT ONEHEADER FILE=patab001")

    ctl_content = "\n".join(ctl_lines)
    os.makedirs(os.path.dirname(os.path.abspath(output_path)), exist_ok=True)
    with open(output_path, "w") as f:
        f.write(ctl_content)

    log.append(f"\nControl stream written to: {output_path}")
    log.append(f"Parameters ({len(params)} fixed effects + IIV): {', '.join(params.keys())}")
    log.append("\n── Next Steps ──")
    log.append("  1. Verify $INPUT column order matches your dataset header.")
    log.append("  2. Review initial THETA estimates — should be within 10× of truth.")
    log.append("  3. Run: execute run001.ctl (PsN) or nm run001.ctl")
    log.append("  4. Check covariance step — successful = stable model.")
    log.append("  5. Evaluate diagnostics: GOF plots, CWRES vs TIME/PRED, VPC.")

    return "\n".join(log)


