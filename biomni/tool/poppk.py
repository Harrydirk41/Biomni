def compare_pk_models(
    model_results: list,
) -> str:
    """Compare population PK models and recommend the best based on standard criteria.

    Applies AIC, BIC, OFV likelihood ratio test, VPC pass/fail, and
    parsimony criteria. Outputs a ranked comparison table.

    Parameters
    ----------
    model_results : list
        List of dicts, each describing one model. Required keys:
        'name', 'ofv', 'n_params', 'n_obs', 'cov_successful', 'vpc_pass'.
        Optional: 'notes'.
        Example:
          [{'name': '1cmt_oral', 'ofv': 1200.5, 'n_params': 4,
            'n_obs': 800, 'cov_successful': True, 'vpc_pass': True},
           {'name': '2cmt_oral', 'ofv': 1185.2, 'n_params': 6,
            'n_obs': 800, 'cov_successful': True, 'vpc_pass': True}]
    """
    log = ["=" * 60]
    log.append("MODEL COMPARISON AND SELECTION")
    log.append("=" * 60)

    if not model_results:
        return "No model results provided."

    for m in model_results:
        m["aic"] = m["ofv"] + 2 * m["n_params"]
        m["bic"] = m["ofv"] + m["n_params"] * np.log(m["n_obs"])

    best_aic = min(model_results, key=lambda x: x["aic"])["aic"]

    log.append(f"\n{'Model':<20} {'OFV':>10} {'P':>5} {'AIC':>10} {'ΔAIC':>8} {'BIC':>10} {'Cov':>6} {'VPC':>6}")
    log.append("-" * 80)
    for m in sorted(model_results, key=lambda x: x["aic"]):
        d_aic = m["aic"] - best_aic
        cov_s = "✓" if m.get("cov_successful") else "✗"
        vpc_s = "✓" if m.get("vpc_pass") else "✗"
        log.append(
            f"  {m['name']:<18} {m['ofv']:>10.2f} {m['n_params']:>5} "
            f"{m['aic']:>10.2f} {d_aic:>8.2f} {m['bic']:>10.2f} {cov_s:>6} {vpc_s:>6}"
        )

    # LRT between consecutive models (sorted by n_params)
    sorted_models = sorted(model_results, key=lambda x: x["n_params"])
    log.append("\n── Likelihood Ratio Tests (LRT) ──")
    log.append("  χ² critical: df=1 → 3.84 (p<0.05), df=2 → 5.99, df=3 → 7.81")
    for i in range(len(sorted_models) - 1):
        base = sorted_models[i]
        full = sorted_models[i + 1]
        delta_ofv = base["ofv"] - full["ofv"]
        delta_p = full["n_params"] - base["n_params"]
        from scipy.stats import chi2
        p_val = 1 - chi2.cdf(delta_ofv, df=delta_p) if delta_ofv > 0 else 1.0
        sig = "✓ significant" if p_val < 0.05 else "not significant"
        log.append(
            f"  {base['name']} → {full['name']}: "
            f"ΔOFV={delta_ofv:.2f}, Δp={delta_p}, p={p_val:.4f}  [{sig}]"
        )

    # Recommendation
    log.append("\n── Recommendation ──")
    candidates = [m for m in model_results if m.get("cov_successful") and m.get("vpc_pass")]
    if candidates:
        best = min(candidates, key=lambda x: x["bic"])
        log.append(f"  Recommended model: {best['name']}")
        log.append("  Rationale: lowest BIC among models with successful covariance step and VPC pass.")
    else:
        log.append("  No model passed all criteria. Review covariance step failures and VPC.")

    log.append("\n── Selection Hierarchy ──")
    log.append("  1. Covariance step must succeed (model stability)")
    log.append("  2. VPC must be acceptable (predictive performance)")
    log.append("  3. ΔOFV ≥ 3.84 (LRT, p<0.05) to justify additional parameter")
    log.append("  4. Lowest BIC among qualifying models (parsimony)")
    log.append("  5. Clinical plausibility of parameter estimates")

    return "\n".join(log)
