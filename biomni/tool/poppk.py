def run_nlmixr2_model(
    dataset_path: str,
    model_type: str = "2cmt_oral",
    pk_parameters: dict = None,
    error_model: str = "prop",
    estimation_method: str = "saem",
    output_dir: str = "./nlmixr2_output",
    id_col: str = "ID",
    time_col: str = "TIME",
    dv_col: str = "DV",
    amt_col: str = "AMT",
    evid_col: str = "EVID",
) -> str:
    """Fit a population PK model using nlmixr2 in R.

    Reads the dataset, fits the specified model using SAEM (or FOCEI),
    generates goodness-of-fit plots, and saves parameter estimates.
    Supports 1cmt IV/oral, 2cmt IV/oral, and custom ODE models.

    Parameters
    ----------
    dataset_path : str
        Path to dataset CSV with columns: ID, TIME, DV, AMT, EVID (minimum).
    model_type : str
        '1cmt_iv', '1cmt_oral', '2cmt_iv', '2cmt_oral'.
    pk_parameters : dict
        Initial estimates for PK parameters (optional, uses defaults if None).
    error_model : str
        Residual error: 'prop' (proportional), 'add' (additive), 'prop+add'.
    estimation_method : str
        'saem' or 'focei'.
    output_dir : str
        Directory to save plots and parameter CSV.
    id_col, time_col, dv_col, amt_col, evid_col : str
        Column names in the dataset (in case of non-standard naming).
    """
    log = ["=" * 60]
    log.append("POPULATION PK FITTING — nlmixr2")
    log.append("=" * 60)
    log.append(f"Dataset     : {dataset_path}")
    log.append(f"Model       : {model_type}")
    log.append(f"Method      : {estimation_method.upper()}")

    os.makedirs(output_dir, exist_ok=True)

    model_funcs = {
        "1cmt_oral": """
  ini({
    lCL  ~ 1.5; lVc ~ 4.0; lKA ~ 0.0
    eta.CL ~ 0.04; eta.Vc ~ 0.04; eta.KA ~ 0.09
    prop.err <- 0.1
  })
  model({
    CL <- exp(lCL + eta.CL)
    Vc <- exp(lVc + eta.Vc)
    KA <- exp(lKA + eta.KA)
    linCmt() ~ prop(prop.err)
  })""",
        "2cmt_oral": """
  ini({
    lCL  ~ 1.5; lVc ~ 3.5; lQ ~ 0.7; lVp ~ 4.5; lKA ~ 0.0
    eta.CL ~ 0.04; eta.Vc ~ 0.04; eta.Q ~ 0.04; eta.Vp ~ 0.04; eta.KA ~ 0.09
    prop.err <- 0.1
  })
  model({
    CL <- exp(lCL + eta.CL)
    Vc <- exp(lVc + eta.Vc)
    Q  <- exp(lQ  + eta.Q)
    Vp <- exp(lVp + eta.Vp)
    KA <- exp(lKA + eta.KA)
    linCmt() ~ prop(prop.err)
  })""",
    }

    model_block = model_funcs.get(model_type, model_funcs["2cmt_oral"])

    r_code = f"""
suppressPackageStartupMessages({{
  library(nlmixr2)
  library(ggplot2)
  library(xpose.nlmixr2)
  library(vpc)
  library(dplyr)
}})

# Load data
pk_data <- read.csv("{dataset_path}")
names(pk_data)[names(pk_data) == "{id_col}"]   <- "ID"
names(pk_data)[names(pk_data) == "{time_col}"] <- "TIME"
names(pk_data)[names(pk_data) == "{dv_col}"]   <- "DV"
names(pk_data)[names(pk_data) == "{amt_col}"]  <- "AMT"
names(pk_data)[names(pk_data) == "{evid_col}"] <- "EVID"

cat("Data loaded:", nrow(pk_data), "rows,", length(unique(pk_data$ID)), "subjects\\n")

# Define model
pk_model <- function() {{
{model_block}
}}

# Fit model
cat("Fitting model with {estimation_method.upper()}...\\n")
fit <- nlmixr2(pk_model, pk_data, est = "{estimation_method}",
               control = {estimation_method}Control(print = 0))

# Print summary
cat("\\nPARAMETER_ESTIMATES_START\\n")
print(fit$parFixed)
cat("PARAMETER_ESTIMATES_END\\n")

cat("\\nVARIANCE_COMPONENTS_START\\n")
print(fit$omega)
cat("VARIANCE_COMPONENTS_END\\n")

cat("\\nOFV:", fit$objf, "\\n")

# Save estimates
write.csv(as.data.frame(fit$parFixed), "{output_dir}/param_estimates.csv")

# GOF plots
gof_data <- data.frame(
  PRED  = fit$PRED,
  IPRED = fit$IPRED,
  DV    = fit$DV,
  CWRES = fit$CWRES,
  TIME  = fit$TIME
)

p1 <- ggplot(gof_data, aes(x = PRED, y = DV)) +
  geom_point(alpha = 0.4, colour = "#2166AC") +
  geom_abline(slope = 1, intercept = 0, colour = "red", linetype = 2) +
  geom_smooth(method = "loess", se = TRUE, colour = "#4DAC26") +
  labs(title = "DV vs Population Prediction (PRED)",
       x = "PRED", y = "DV") + theme_bw(base_size = 12)

p2 <- ggplot(gof_data, aes(x = IPRED, y = DV)) +
  geom_point(alpha = 0.4, colour = "#D6604D") +
  geom_abline(slope = 1, intercept = 0, colour = "red", linetype = 2) +
  geom_smooth(method = "loess", se = TRUE, colour = "#4DAC26") +
  labs(title = "DV vs Individual Prediction (IPRED)",
       x = "IPRED", y = "DV") + theme_bw(base_size = 12)

p3 <- ggplot(gof_data, aes(x = TIME, y = CWRES)) +
  geom_point(alpha = 0.4, colour = "#762A83") +
  geom_hline(yintercept = c(-2, 0, 2), linetype = c(2, 1, 2),
             colour = c("red", "black", "red")) +
  geom_smooth(method = "loess", se = FALSE, colour = "#4DAC26") +
  labs(title = "CWRES vs TIME", x = "Time", y = "CWRES") +
  theme_bw(base_size = 12)

p4 <- ggplot(gof_data, aes(x = PRED, y = CWRES)) +
  geom_point(alpha = 0.4, colour = "#1A6648") +
  geom_hline(yintercept = c(-2, 0, 2), linetype = c(2, 1, 2),
             colour = c("red", "black", "red")) +
  geom_smooth(method = "loess", se = FALSE, colour = "#4DAC26") +
  labs(title = "CWRES vs PRED", x = "PRED", y = "CWRES") +
  theme_bw(base_size = 12)

library(patchwork)
gof_combined <- p1 + p2 + p3 + p4 + plot_layout(ncol = 2)
ggsave("{output_dir}/gof_plots.png", gof_combined, width = 12, height = 10, dpi = 150)

# VPC
cat("Generating VPC...\\n")
vpc_fit <- vpc_ui(fit, n = 500,
                  show = list(obs_dv = TRUE, pi = TRUE, ci = TRUE),
                  title = "Visual Predictive Check ({model_type})")
ggsave("{output_dir}/vpc.png", vpc_fit, width = 10, height = 6, dpi = 150)

cat("\\nFitting complete. Output saved to {output_dir}\\n")
"""

    r_output = run_r_code(r_code)

    if "Error" in r_output and "PARAMETER_ESTIMATES_START" not in r_output:
        log.append(f"\nR execution error:\n{r_output}")
        return "\n".join(log)

    log.append("\n── Parameter Estimates ──")
    in_block = False
    for line in r_output.splitlines():
        if "PARAMETER_ESTIMATES_START" in line:
            in_block = True
            continue
        if "PARAMETER_ESTIMATES_END" in line:
            in_block = False
            continue
        if in_block:
            log.append(f"  {line}")

    # Extract OFV
    ofv_match = re.search(r"OFV:\s*([-\d.]+)", r_output)
    if ofv_match:
        log.append(f"\n  OFV: {ofv_match.group(1)}")

    log.append(f"\nOutput files saved to {output_dir}/:")
    log.append("  param_estimates.csv  — fixed + random effects table")
    log.append("  gof_plots.png        — 4-panel GOF diagnostic plots")
    log.append("  vpc.png              — visual predictive check")

    log.append("\n── Diagnostic Interpretation ──")
    log.append("  GOF: DV vs PRED/IPRED should scatter around identity line with no trend.")
    log.append("  CWRES: should be N(0,1) distributed; |CWRES| > 4 are outliers.")
    log.append("  VPC: 80% of observations should fall within 10th–90th PI bands.")

    return "\n".join(log)


# ─────────────────────────────────────────────────────────────────────────────
# mrgsolve PK/PD Simulation
# ─────────────────────────────────────────────────────────────────────────────

def run_mrgsolve_simulation(
    model_type: str = "2cmt_oral",
    pk_parameters: dict = None,
    dosing_regimen: dict = None,
    n_subjects: int = 100,
    iiv_cv_percent: float = 30.0,
    simulation_duration_h: float = 168.0,
    output_dir: str = "./mrgsolve_output",
    compound_name: str = "compound",
    target_conc: float = None,
) -> str:
    """Simulate PK concentration-time profiles using mrgsolve in R.

    Performs stochastic population simulation incorporating inter-individual
    variability (IIV). Generates mean ± 90% prediction interval plots
    and calculates AUC, Cmax, Cmin at steady state.

    Parameters
    ----------
    model_type : str
        '1cmt_oral', '1cmt_iv', '2cmt_oral', '2cmt_iv'.
    pk_parameters : dict
        Population mean PK parameters (CL, Vc, etc.).
    dosing_regimen : dict
        Dosing scheme. Keys: 'dose_mg', 'interval_h', 'n_doses', 'route'.
        Example: {'dose_mg': 100, 'interval_h': 24, 'n_doses': 7, 'route': 'oral'}
    n_subjects : int
        Number of virtual subjects to simulate.
    iiv_cv_percent : float
        IIV as % CV (applied lognormally to all PK parameters).
    simulation_duration_h : float
        Total simulation time in hours.
    output_dir : str
        Directory to save plots and simulation output.
    compound_name : str
        Compound identifier for plot labels.
    target_conc : float
        Target therapeutic concentration (for % time above target calculation).
    """
    log = ["=" * 60]
    log.append("PK/PD SIMULATION — mrgsolve")
    log.append("=" * 60)
    log.append(f"Compound    : {compound_name}")
    log.append(f"Model       : {model_type}")
    log.append(f"N subjects  : {n_subjects}")
    log.append(f"IIV (%CV)   : {iiv_cv_percent}%")

    os.makedirs(output_dir, exist_ok=True)

    defaults_pk = {
        "1cmt_oral": {"CL": 5.0, "Vc": 50.0, "KA": 1.0},
        "1cmt_iv":   {"CL": 5.0, "Vc": 50.0},
        "2cmt_oral": {"CL": 5.0, "Vc": 30.0, "Q": 2.0, "Vp": 80.0, "KA": 1.0},
        "2cmt_iv":   {"CL": 5.0, "Vc": 30.0, "Q": 2.0, "Vp": 80.0},
    }
    params = pk_parameters or defaults_pk.get(model_type, {"CL": 5.0, "Vc": 50.0, "KA": 1.0})
    dosing = dosing_regimen or {"dose_mg": 100, "interval_h": 24, "n_doses": 7, "route": "oral"}

    params_r = ", ".join(f"{k} = {v}" for k, v in params.items())
    iiv_omega = (iiv_cv_percent / 100) ** 2
    _target_line = (
        "cat(sprintf('Time above target (%.1f): %.1f%%\\n', "
        + str(target_conc or 1.0)
        + ", 100 * mean(last_interval$Cp > "
        + str(target_conc or 1.0)
        + ")))"
        if target_conc
        else ""
    )

    r_code = f"""
suppressPackageStartupMessages({{
  library(mrgsolve)
  library(ggplot2)
  library(dplyr)
}})

set.seed(20240101)
N <- {n_subjects}

# Build model code
model_code <- '
$PARAM {params_r}
$CMT  {"DEPOT CENTRAL PERIPH" if "oral" in model_type else ("CENTRAL PERIPH" if "2cmt" in model_type else "CENTRAL")}
$GLOBAL double Cp = 0;
$ODE
{"dxdt_DEPOT   = -KA * DEPOT;" if "oral" in model_type else ""}
{"dxdt_CENTRAL = KA * DEPOT - (CL/Vc) * CENTRAL - (Q/Vc) * CENTRAL + (Q/Vp) * PERIPH;" if "2cmt" in model_type else "dxdt_CENTRAL = -CL/Vc * CENTRAL;"}
{"dxdt_PERIPH  = (Q/Vc) * CENTRAL - (Q/Vp) * PERIPH;" if "2cmt" in model_type else ""}
$TABLE double Cp = CENTRAL / Vc; capture Cp;
'

mod <- mread_cache("pkmodel", tempdir(), model_code)

# IIV via parameter variation (lognormal)
omega <- {iiv_omega}
idata <- data.frame(
  ID  = 1:N,
  CL  = {params.get("CL", 5)} * exp(rnorm(N, 0, sqrt(omega))),
  Vc  = {params.get("Vc", 50)} * exp(rnorm(N, 0, sqrt(omega))),
  {"KA  = " + str(params.get("KA", 1.0)) + " * exp(rnorm(N, 0, sqrt(omega)))," if "oral" in model_type else ""}
  {"Q   = " + str(params.get("Q", 2.0)) + " * exp(rnorm(N, 0, sqrt(omega)))," if "2cmt" in model_type else ""}
  {"Vp  = " + str(params.get("Vp", 80.0)) + " * exp(rnorm(N, 0, sqrt(omega)))," if "2cmt" in model_type else ""}
  stringsAsFactors = FALSE
)

# Dosing events
dose_event <- ev(
  amt  = {dosing.get("dose_mg", 100)},
  ii   = {dosing.get("interval_h", 24)},
  addl = {dosing.get("n_doses", 7) - 1},
  cmt  = {"1" if "oral" in model_type else "1"}
)

# Simulate
sim <- mod %>%
  idata_set(idata) %>%
  ev(dose_event) %>%
  mrgsim(end = {simulation_duration_h}, delta = 0.5) %>%
  as.data.frame()

# Summary statistics
sim_summary <- sim %>%
  group_by(time) %>%
  summarise(
    median = median(Cp),
    p5     = quantile(Cp, 0.05),
    p95    = quantile(Cp, 0.95),
    p10    = quantile(Cp, 0.10),
    p90    = quantile(Cp, 0.90),
    .groups = "drop"
  )

# Steady-state metrics (last dosing interval)
last_interval <- sim %>%
  filter(time > {simulation_duration_h - dosing.get("interval_h", 24)})
ss_auc   <- mean(tapply(last_interval$Cp, last_interval$ID,
                        function(x) sum(diff(unique(last_interval$time)) * x[-1])))
ss_cmax  <- median(tapply(last_interval$Cp, last_interval$ID, max))
ss_cmin  <- median(tapply(last_interval$Cp, last_interval$ID, min))

cat("\\nSS_METRICS_START\\n")
cat(sprintf("Steady-state Cmax (median): %.3f ng/mL\\n", ss_cmax))
cat(sprintf("Steady-state Cmin (median): %.3f ng/mL\\n", ss_cmin))
cat(sprintf("Steady-state AUCtau (mean): %.1f ng.h/mL\\n", ss_auc))
{_target_line}
cat("SS_METRICS_END\\n")

# Plot
p <- ggplot(sim_summary, aes(x = time)) +
  geom_ribbon(aes(ymin = p5, ymax = p95), fill = "#2166AC", alpha = 0.15) +
  geom_ribbon(aes(ymin = p10, ymax = p90), fill = "#2166AC", alpha = 0.25) +
  geom_line(aes(y = median), colour = "#2166AC", linewidth = 1.2) +
  {"geom_hline(yintercept = " + str(target_conc) + ", colour = 'red', linetype = 2, linewidth = 0.8) +" if target_conc else ""}
  labs(
    title    = "PK Simulation — {compound_name}",
    subtitle = "Median (line), 80% PI (dark band), 90% PI (light band)",
    x        = "Time (h)",
    y        = "Plasma Concentration (ng/mL)"
  ) +
  theme_bw(base_size = 13)
ggsave("{output_dir}/pk_simulation.png", p, width = 10, height = 6, dpi = 150)

write.csv(sim_summary, "{output_dir}/sim_summary.csv", row.names = FALSE)
cat("Simulation saved.\\n")
"""

    r_output = run_r_code(r_code)

    if "Error" in r_output and "SS_METRICS_START" not in r_output:
        log.append(f"\nR execution error:\n{r_output}")
        return "\n".join(log)

    log.append(f"\nDosing regimen: {dosing.get('dose_mg')} mg every {dosing.get('interval_h')} h × {dosing.get('n_doses')} doses")
    log.append(f"\n── Steady-State PK Metrics (N={n_subjects} virtual subjects) ──")
    in_block = False
    for line in r_output.splitlines():
        if "SS_METRICS_START" in line:
            in_block = True
            continue
        if "SS_METRICS_END" in line:
            in_block = False
            continue
        if in_block:
            log.append(f"  {line}")

    log.append(f"\nOutput saved to {output_dir}/:")
    log.append("  pk_simulation.png  — median + 80/90% PI band plot")
    log.append("  sim_summary.csv    — time-course summary statistics")

    return "\n".join(log)


