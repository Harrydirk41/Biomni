def parse_nonmem_output(
    lst_file_path: str,
    ext_file_path: str = None,
    cov_file_path: str = None,
) -> str:
    """Parse NONMEM .lst output file to extract parameter estimates and diagnostics.

    Extracts: objective function value (OFV), THETA estimates and RSE%,
    OMEGA (IIV) estimates, SIGMA estimates, covariance step status,
    condition number, and run summary.

    Parameters
    ----------
    lst_file_path : str
        Path to NONMEM .lst output file.
    ext_file_path : str
        Path to NONMEM .ext file (extended output, more precise estimates).
    cov_file_path : str
        Path to NONMEM .cov file (covariance matrix output).
    """
    log = ["=" * 60]
    log.append("NONMEM OUTPUT PARSER")
    log.append("=" * 60)
    log.append(f"LST file: {lst_file_path}")

    if not os.path.exists(lst_file_path):
        return f"Error: LST file not found at {lst_file_path}"

    with open(lst_file_path, "r") as f:
        lst_content = f.read()

    # OFV
    ofv_match = re.search(r"#OBJV:\s*\*?\s*([-\d.]+)", lst_content)
    if ofv_match:
        ofv = float(ofv_match.group(1))
        log.append("\n── Objective Function ──")
        log.append(f"  OFV (NONMEM): {ofv:.3f}")
        log.append("  AIC ≈ OFV + 2p  (p = number of estimated parameters)")
        log.append("  BIC ≈ OFV + p × ln(N)  (N = number of observations)")

    # Minimisation status
    if "MINIMIZATION SUCCESSFUL" in lst_content:
        log.append("\n  ✓ Minimisation: SUCCESSFUL")
    elif "MINIMIZATION TERMINATED" in lst_content:
        log.append("\n  ⚠ Minimisation: TERMINATED (check MAXEVAL, rounding errors)")
    elif "ROUNDING ERRORS" in lst_content:
        log.append("\n  ⚠ Minimisation: ROUNDING ERRORS — parameter estimates unreliable")

    # Covariance step
    if "COVARIANCE STEP ABORTED" in lst_content:
        log.append("  ⚠ Covariance step: ABORTED — model may be over-parameterised")
        log.append("    Try fixing problematic parameters or simplifying model.")
    elif "STANDARD ERROR OF ESTIMATE" in lst_content:
        log.append("  ✓ Covariance step: SUCCESSFUL")

    # THETA extraction
    theta_section = re.search(
        r"THETA - VECTOR OF FIXED EFFECTS PARAMETERS.*?\n(.*?)(?=OMEGA|SIGMA|#)",
        lst_content, re.DOTALL
    )
    if theta_section:
        log.append("\n── THETA (Fixed Effects) ──")
        theta_vals = re.findall(r"[-+]?\d*\.?\d+(?:[Ee][+-]?\d+)?", theta_section.group(1))
        for i, v in enumerate(theta_vals[:20], 1):
            log.append(f"  THETA({i}): {v}")

    # SE/RSE extraction
    se_section = re.search(
        r"STANDARD ERROR OF ESTIMATE.*?\n(.*?)(?=COVARIANCE|#|\Z)",
        lst_content, re.DOTALL
    )
    if se_section and theta_section:
        theta_vals_f = [float(v) for v in re.findall(r"[-+]?\d*\.?\d+(?:[Ee][+-]?\d+)?", theta_section.group(1))[:20]]
        se_vals = re.findall(r"[-+]?\d*\.?\d+(?:[Ee][+-]?\d+)?", se_section.group(1))[:20]
        log.append("\n── RSE% (Relative Standard Error) ──")
        log.append("  RSE < 25% considered acceptable for fixed effects")
        log.append("  RSE < 50% considered acceptable for variance parameters")
        for i, (th, se) in enumerate(zip(theta_vals_f, se_vals), 1):
            try:
                rse = abs(float(se) / th * 100)
                flag = "  ⚠ HIGH" if rse > 50 else ("  ⚠" if rse > 25 else "  ✓")
                log.append(f"  THETA({i}): estimate={th:.4g}  SE={se}  RSE={rse:.1f}%{flag}")
            except Exception:
                pass

    # Condition number
    cn_match = re.search(r"CONDITION NUMBER OF FINAL R MATRIX:\s*([\d.E+\-]+)", lst_content)
    if cn_match:
        cn = float(cn_match.group(1))
        log.append("\n── Condition Number ──")
        log.append(f"  Condition number: {cn:.2e}")
        if cn > 1e7:
            log.append("  ⚠ Condition number > 10⁷ — near-singular matrix; model instability.")
        elif cn > 1e4:
            log.append("  Condition number 10⁴–10⁷ — marginal stability; review correlation.")
        else:
            log.append("  ✓ Condition number < 10⁴ — stable model.")

    log.append("\n── Interpretation Guide ──")
    log.append("  Acceptable model criteria:")
    log.append("  • Minimisation successful")
    log.append("  • Covariance step successful")
    log.append("  • RSE < 25% for THETAs, < 50% for OMEGAs/SIGMAs")
    log.append("  • Condition number < 10⁷")
    log.append("  • No ETA or EPS shrinkage > 30% (check GOF plots)")

    return "\n".join(log)


# ─────────────────────────────────────────────────────────────────────────────
